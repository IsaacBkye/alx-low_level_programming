#include "3-calc.h"
/**
 * op_add - Adds
 * @a: arg
 * @b: arg2
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Subtracts
 * @a: arg
 * @b: arg1
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplies
 * @a: arg
 * @b: arg1
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides
 * @a: arg
 * @b: arg1
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Modulo
 * @a: arg
 * @b: arg1
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
